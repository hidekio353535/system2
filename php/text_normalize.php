<?php
/**
 * 結合濁点(U+3099)・結合半濁点(U+309A)を含む NFD 分解形の文字列を、
 * 合成済み(NFC)のかなへ変換する。
 *
 * Mac 等で入力されたデータは「シ + U+3099」のように濁点が結合文字として
 * 分離保存される事があり、TCPDF(CIDフォント)が結合文字を描画できないため
 * PDF/印刷で濁点・半濁点が脱落する（例: ジョイフーズ → ショイフース）。
 * その対策として出力直前に本関数で合成済みの文字へ変換する。
 *
 * PHP 5.2 には Normalizer(intl) が無いため、かなの濁点・半濁点合成のみを
 * 明示マップで実装する。結合文字を含まない文字列はそのまま返す。
 */
function nfc($str) {
	if ($str === null || $str === '') {
		return $str;
	}
	// 結合濁点(E3 82 99) / 結合半濁点(E3 82 9A) を含まなければ何もしない
	if (strpos($str, "\xe3\x82\x99") === false && strpos($str, "\xe3\x82\x9a") === false) {
		return $str;
	}

	static $voiced = null;
	static $semi = null;
	if ($voiced === null) {
		// 基底かな => 濁点付き合成済みかな
		$voiced = array(
			// ひらがな
			'か'=>'が','き'=>'ぎ','く'=>'ぐ','け'=>'げ','こ'=>'ご',
			'さ'=>'ざ','し'=>'じ','す'=>'ず','せ'=>'ぜ','そ'=>'ぞ',
			'た'=>'だ','ち'=>'ぢ','つ'=>'づ','て'=>'で','と'=>'ど',
			'は'=>'ば','ひ'=>'び','ふ'=>'ぶ','へ'=>'べ','ほ'=>'ぼ',
			'う'=>'ゔ',
			// カタカナ
			'カ'=>'ガ','キ'=>'ギ','ク'=>'グ','ケ'=>'ゲ','コ'=>'ゴ',
			'サ'=>'ザ','シ'=>'ジ','ス'=>'ズ','セ'=>'ゼ','ソ'=>'ゾ',
			'タ'=>'ダ','チ'=>'ヂ','ツ'=>'ヅ','テ'=>'デ','ト'=>'ド',
			'ハ'=>'バ','ヒ'=>'ビ','フ'=>'ブ','ヘ'=>'ベ','ホ'=>'ボ',
			'ウ'=>'ヴ','ワ'=>'ヷ','ヰ'=>'ヸ','ヱ'=>'ヹ','ヲ'=>'ヺ',
		);
		// 基底かな => 半濁点付き合成済みかな
		$semi = array(
			'は'=>'ぱ','ひ'=>'ぴ','ふ'=>'ぷ','へ'=>'ぺ','ほ'=>'ぽ',
			'ハ'=>'パ','ヒ'=>'ピ','フ'=>'プ','ヘ'=>'ペ','ホ'=>'ポ',
		);
	}

	$chars = preg_split('//u', $str, -1, PREG_SPLIT_NO_EMPTY);
	$n = count($chars);
	$out = '';
	for ($i = 0; $i < $n; $i++) {
		$c = $chars[$i];
		$next = ($i + 1 < $n) ? $chars[$i + 1] : '';
		if ($next === "\xe3\x82\x99" && isset($voiced[$c])) {        // U+3099 結合濁点
			$out .= $voiced[$c];
			$i++;
		} else if ($next === "\xe3\x82\x9a" && isset($semi[$c])) {   // U+309A 結合半濁点
			$out .= $semi[$c];
			$i++;
		} else {
			$out .= $c;
		}
	}
	return $out;
}
